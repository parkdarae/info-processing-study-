def apply_translations():
    """번역을 적용하여 JSONL 파일 업데이트"""
    
    input_path = r"C:\Users\darae\Desktop\info_ver5\data\items_cissp.jsonl"
    
    # 기존 데이터 읽기
    with open(input_path, "r", encoding="utf-8") as f:
        questions = [json.loads(line) for line in f]
    
    print(f"[INFO] Total questions: {len(questions)}")
    
    # 번역 적용
    updated = 0
    for q in questions:
        q_no = q["q_no"]
        if q_no in translations:
            q["question_ko"] = translations[q_no]["question_ko"]
            q["choices_ko"] = translations[q_no]["choices_ko"]
            updated += 1
    
    # 저장
    with open(input_path, "w", encoding="utf-8") as f:
        for q in questions:
            f.write(json.dumps(q, ensure_ascii=False) + "\n")
    
    print(f"[OK] Updated {updated} questions with Korean translations (301-400)")
    
    # 검증
    translated = sum(1 for q in questions if q.get("question_ko"))
    print(f"[INFO] Total translated: {translated} / {len(questions)}")


