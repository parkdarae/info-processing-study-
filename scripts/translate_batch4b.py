# JSONL 파일 업데이트
def update_translations():
    input_file = r"C:\Users\darae\Desktop\info_ver5\data\items_cissp.jsonl"
    
    # 파일 읽기
    with open(input_file, 'r', encoding='utf-8') as f:
        lines = f.readlines()
    
    updated_count = 0
    updated_lines = []
    
    for line in lines:
        if line.strip():
            item = json.loads(line)
            q_no = item.get('q_no', '')
            
            if q_no in translations:
                item['question_ko'] = translations[q_no]['question_ko']
                item['choices_ko'] = translations[q_no]['choices_ko']
                updated_count += 1
            
            updated_lines.append(json.dumps(item, ensure_ascii=False) + '\n')
        else:
            updated_lines.append(line)
    
    # 파일 쓰기
    with open(input_file, 'w', encoding='utf-8') as f:
        f.writelines(updated_lines)
    
    print(f"번역 완료: {updated_count}개 문제 업데이트됨 (601-700)")
    return updated_count

if __name__ == "__main__":
    update_translations()

